//! Only 3 is max currently
void randomSprites(int nrOfSprites)
{
    for (int i = 0; i < MAPLENGHT; i++)
    {
        for (int j = 0; j < MAPHEIGTH; j++)
        {
            int random = rand() % (nrOfSprites + 1);
            // cout << random << " ";
            switch (random)
            {
            case 0:
                background[i][j] = &caster_pixel_data;
                break;
            case 1:
                background[i][j] = &guard_pixel_data;
                break;
            case 2:
                background[i][j] = &medic_pixel_data;
                break;
            default:
                background[i][j] = &medic_pixel_data;
                break;
            }
        }
    }
}

//~64ms on 10000 tries
void moveSpritesWithMemcpy()
{
    int offset;
    unsigned char(*selected_array)[32];
    for (int i = 0; i < NUM_LINE_SCREEN; i++)
    {
        for (int j = 0; j < MAPLENGHT; j++)
        {
            selected_array = *background[i / 32][j];
            // printLine(selected_array[30], 32);
            memcpy(genBuffer[0] + j * 32, selected_array[i % 32], 32);
            // cout << "j: " << j<< " ";
        }
        sendLine();
        // cout<<endl<<endl;
        // printLine(genBuffer[0], 320);
    }
}

void moveSpritesWithforCycle()
{
    unsigned char(*selected_array)[32];
    for (int i = 0; i < NUM_LINE_SCREEN; i++)
    {
        for (int j = 0; j < MAPLENGHT; j++)
        {
            int offset = (j * 32);
            // cout << "j: " << j << " ";
            selected_array = *background[i / 32][j];
            // printLine(selected_array[30], 32);
            for (int k = 0; k < 32; k++)
            {
                genBuffer[0][k + offset] = selected_array[i % 32][k];
            }
        }
        // cout << endl << endl;
        // printLine(genBuffer[0], 320);
        sendLine();
    }
}

